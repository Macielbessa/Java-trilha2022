package com.basico.controledefluxo;

public class whileatv {
    public static void main(String[]args){
        int contador = 0;
        while(contador < 10){
            System.out.println();
            ++ contador;
            // contador += 1;
           // contador = contador + 1;
        }
        System.out.println(contador);
    }
}
//O while aceita receber dois valores, true e false.
// O valor true mantém o loop em execução, já o false o finaliza